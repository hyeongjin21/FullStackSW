package 단순if문;

public class Ex01단순if문 {

	public static void main(String[] args) {

		// 조건문
		// : 주어진 조건을 비교 판단한다
		// 조건을 만족한다면(true) -> 지정된 문장을 실행
		// 그렇지 않다면 (false) -> 지정된 문장을 실행하지 않고 다음 명령을 수행
		
		// 1. 단순 if문
		if(false) { 
			// (boolean 타입의 값 혹은 연산 결과가 와야한다)
			// 조건이 true일 때 실행할 실행 문장
			System.out.println("true일 때 실행하는 문장");
			
		} // {} 부분을 영역 혹은 바디 라고 부른다
		
		System.out.println("조건문 실행 다음에 출력되는 문장");
		
		
		
		
		
		
		
	}

}
